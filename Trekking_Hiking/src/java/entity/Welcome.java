package entity;

/**
 *
 * @author Hiệu Bùi
 */
public class Welcome {
    private String image;
    private String des;

    public Welcome() {
    }

    public Welcome(String image, String des) {
        this.image = image;
        this.des = des;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public String getDes() {
        return des;
    }

    public void setDes(String des) {
        this.des = des;
    }
    
}
